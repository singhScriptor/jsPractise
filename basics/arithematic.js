function add(a,b){
    let Addition= a+b
    return Addition
}
function subtract(a,b){
    let Subtraction= a-b
    return Subtraction
}
function multiply(a,b){
    let Multiplication= a*b
    return Multiplication
}
console.log("Addition :", add(5,3))
console.log("Subtraction :", subtract(5,3))
console.log("Multiplication :", multiply(5,3))